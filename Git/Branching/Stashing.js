/*
Stashing

Staging is storing it in a safe place.

(git stash push -m "message") Create a stash.
(git stash push -am "message") Stash all the new changes. 
(git stash list) shows all the stashes.
(git stash show "#/name") show the changes in that stash.
(git stash apply "#") Apply the stash in the working directory.
(git stash drop "#") delete stash.
(git stash clear) remove all the stashes.

*/