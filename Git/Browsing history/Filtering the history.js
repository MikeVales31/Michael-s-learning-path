/*
Filtering the history

-(git log --oneline -3) to see the first 3 files.
-(git log --oneline --author "author's name") to see only the things that do this author.
-(git log --oneline --before/after = "date")to see all commits of those dates.
-(git log --oneline --before/after = "yesterday/today/one week ago/one month ago...")
-(git log --oneline --grep="text")by word in the message
-(git log --oneline -S"OBJECTIVES")to a function declaration
-(git log --oneline "from down ID first commit".. " to up ID last commit")by a range in commits.
-(git log --oneline "name of the file with extension")commits that touch a particular file.

 */