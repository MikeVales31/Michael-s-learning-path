/*
Finding contributors using shortlog

(git shortlog) See the name, number of commitments and messages.
(git shortlog -n) Sort for the number of commits per author.
(git shortlog -n -s) Sort for the number of commits per author and supress the messages.
(git shortlog -n -s -e) Sort for the number of commits per author, supress the messages and show the mail.
(git shortlog -n -s -e --before/after="") to see the commits of a date.
*/