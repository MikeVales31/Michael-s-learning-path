/*
Contributing to open source projects

Create an issue with a bug or feature you want to implement
and because you don't have access to push, you go to the top and fork the repository.

That will create a copy of the repository in your account and you can manipulate by 
creating new branches, new tags, you can push your stuff, you can create anything.

The you clone it in your local machine, do the changes and push them.
Create a pull request (note that you will have your main repository and the head repository)

Only the mainteiner can merge and close pull request.
*/